package service;

public class EmployerScore {
	private int employerScoreNo;
	private int employerNo;
	private int score;
	
	public int getEmployerScoreNo() {
		return employerScoreNo;
	}
	public void setEmployerScoreNo(int employerScoreNo) {
		this.employerScoreNo = employerScoreNo;
	}
	public int getEmployerNo() {
		return employerNo;
	}
	public void setEmployerNo(int employerNo) {
		this.employerNo = employerNo;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
}
