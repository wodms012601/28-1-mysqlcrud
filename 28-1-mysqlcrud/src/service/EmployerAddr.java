package service;

public class EmployerAddr {
	private int employerAddrNo;
	private int employerNo;
	private String employerAddrContent;
	
	public int getEmployerAddrNo() {
		return employerAddrNo;
	}
	public void setEmployerAddrNo(int employerAddrNo) {
		this.employerAddrNo = employerAddrNo;
	}
	public int getEmployerNo() {
		return employerNo;
	}
	public void setEmployerNo(int employerNo) {
		this.employerNo = employerNo;
	}
	public String getEmployerAddrContent() {
		return employerAddrContent;
	}
	public void setEmployerAddrContent(String employerAddrContent) {
		this.employerAddrContent = employerAddrContent;
	}
}
