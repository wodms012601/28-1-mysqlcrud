package service;

public class Employer {
	private int employerNo;
	private String employerName;
	private int employerAge;
	
	public int getEmployerNo() {
		return employerNo;
	}
	public void setEmployerNo(int employerNo) {
		this.employerNo = employerNo;
	}
	public String getEmployerName() {
		return employerName;
	}
	public void setEmployerName(String employerName) {
		this.employerName = employerName;
	}
	public int getEmployerAge() {
		return employerAge;
	}
	public void setEmployerAge(int employerAge) {
		this.employerAge = employerAge;
	}
}
